#!/bin/bash

# Проверяем, есть ли входные аргументы
if [ "$#" -eq 0 ]; then
    echo "Нет входных аргументов."
    exit 1
fi

# Считаем количество аргументов
count=$#

# Суммируем входные аргументы
sum=0
for num in "$@"; do
    sum=$((sum + num))
done

# Вычисляем среднее арифметическое
average=$(echo "scale=2; $sum / $count" | bc)

# Выводим результаты
echo "Количество чисел: $count"
echo "Среднее арифметическое: $average"
